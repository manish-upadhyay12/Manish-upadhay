package TakeInput;
import java.util.*;

public class FirstCode {
    public static void main(String[]args){
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        long b = sc.nextLong();
        float c = sc.nextFloat();
        char ch = sc.next().charAt(0);
        System.out.print(a);
        System.out.print(b);
        System.out.print(c);
        System.out.print(ch);
    }
}
